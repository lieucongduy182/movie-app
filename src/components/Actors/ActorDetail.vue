<script>
export default {
    data() {
        return {
            actor: {},
            knownFor: [],
        };
    },
    mounted() {
        this.fetchActorDetail(this.$route.params.id);
        this.fetchKnownFor(this.$route.params.id);
    },
    computed: {
        imageProfilePath() {
            let profile_path = this.actor.profile_path;
            if (profile_path) {
                return `https://image.tmdb.org/t/p/w300${profile_path}`;
            } else {
                return `http://via.placeholder.com/185x185`;
            }
        },
    },
    methods: {
        async fetchActorDetail(actorId) {
            const response = await this.$http.get(`/person/${actorId}`);
            this.actor = response.data;
        },
        async fetchKnownFor(actorId) {
            const response = await this.$http.get(
                `/person/${actorId}/combined_credits`
            );
            this.knownFor = response.data.cast
                .filter((x) => (x.media_type = 'movie'))
                .slice(0, 5);
        },
        imagePosterPath(poster_path) {
            if (poster_path) {
                return `https://image.tmdb.org/t/p/w300${poster_path}`;
            } else {
                return `http://via.placeholder.com/185x185`;
            }
        },
    },
};
